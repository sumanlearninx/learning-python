# arithmetic operators : +, -, *, /, //(integer divison), %(modulo), **(exponentiation)
# comparison operators : == (equals to), != (not equal to), >, <, >=, <=
# logical operators : and, or, not
# membership operators : in, not in
# identity operators : is, is not
# assignment operators : =, +=, -=

num1 = 10
num2 = 20

# arithmetic
print( num1 + num2)
print (num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 // num2)
print(num1 % num2)
print(num1 ** num2)

# comparison
print(num1 == num2) # False
print(num1 != num2) # True
print(num1 > num2) # False
print(num1 < num2) # True
